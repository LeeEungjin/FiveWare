package com.five.ware.mater;

public class MaterDTO {
	
	private String materCode;
	private String materDate;
	private String temp;
	private String name;
	private String materMemo;
	private String materKind;

	public String getMaterCode() {
		return materCode;
	}
	public void setMaterCode(String materCode) {
		this.materCode = materCode;
	}
	public String getMaterDate() {
		return materDate;
	}
	public void setMaterDate(String materDate) {
		this.materDate = materDate;
	}
	public String getTemp() {
		return temp;
	}
	public void setTemp(String temp) {
		this.temp = temp;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getMaterMemo() {
		return materMemo;
	}
	public void setMaterMemo(String materMemo) {
		this.materMemo = materMemo;
	}
	public String getMaterKind() {
		return materKind;
	}
	public void setMaterKind(String materKind) {
		this.materKind = materKind;
	}

	
}
